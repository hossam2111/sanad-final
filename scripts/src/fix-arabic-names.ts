async function fixNames() {
  const client = await pool.connect();
  try {
    let updated = 0;
    for (const [nationalId, fullName] of updates) {
      const res = await client.query(
        `UPDATE patients SET full_name = $1 WHERE national_id = $2`,
        [fullName, nationalId]
      );
      if (res.rowCount && res.rowCount > 0) updated++;
    }
    console.log(`✅ Updated ${updated} / ${updates.length} patients to Arabic names.`);
  } finally {
    client.release();
    await pool.end();
  }
}

fixNames().catch(console.error).finally(() => process.exit(0));
